Complex Silica :: eps(Real eV)
{
  Real n = cubicHermiteSplineInterpolate(ftab,ntab,SILICA_TABLE_SIZE,eV);
  Real k = cubicHermiteSplineInterpolate(ftab,ktab,SILICA_TABLE_SIZE,eV);
  Complex sqrteps(n,k);
  return sqrteps * sqrteps;
}

Complex Silica :: mu(Real eV)
{
  return Complex(1.,0.);
}
